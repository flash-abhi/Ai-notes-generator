import React, { useEffect } from "react";
import { useState } from "react";
import axios from "axios";
import { serverUrl } from "../App";
import { AnimatePresence, motion } from "motion/react";
import { useSelector } from "react-redux";
import { useNavigate } from "react-router-dom";
import {GiHamburgerMenu} from "react-icons/gi";
import { IoArrowBackCircleSharp } from "react-icons/io5";
import FinalResult from "../components/FinalResult";

const History = () => {
  const [topics, setTopics] = useState([]);
  const {userData} = useSelector((state) => state.user);
  const [isSideBarOpen,setIsSidebarOpen] = useState()
  const credits = userData?.credits || 0;
  const [selectedNote, setSelectedNote] = useState(null);
  const [loading, setLoading] = useState(false);
  const navigate = useNavigate();
  useEffect(()=>{
    if(window.innerWidth >= 1024){
      setIsSidebarOpen(true);
    }
  },[]);
  useEffect(() => {
    myNotes();
  }, []);
  const myNotes = async () => {
    try {
      const res = await axios.get(serverUrl + "/api/notes/getnotes", {
        withCredentials: true,
      });
      console.log(res.data);
      setTopics(Array.isArray(res.data) ? res.data : []);
    } catch (error) {
      console.log(error);
    }
  };
  const openNotes = async (noteId) => {
    setLoading(true);
    try {
      const res = await axios.get(`${serverUrl}/api/notes/${noteId}`,{withCredentials:true});
      setSelectedNote(res.data.content);
      setLoading(false);
    } catch (error) {
      console.log(error);
      setLoading(false);
    }
  }
  return (
    <div className="min-h-screen bg-gradient-to-br from-gray-100 to-gray-200 px-6 py-8">
      <motion.header
        initial={{ opacity: 0, y: -15 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.6 }}
        className="mb-4 rounded-2xl bg-black/80 backdrop-blur-xl border border-white/10 px-8 py-6 items-start flex justify-between md:items-center gap-4  md:flex-row shadow-[0_20px_45px_rgba(0,0,0,0,6)]"
      >
        <div
          className="flex flex-col cursor-pointer"
          onClick={() => navigate("/")}
        >
          <div className="flex gap-2">
            <img src="logo.png" alt="logo" className="w-10 h-8" />
            <h1 className="text-xl flex gap-2font-bold bg-gradient-to-r from-white via-gray-300 to-white bg-clip-text text-transparent">
              <span className="font-bold">ENG</span>{" "}
              <div className="w-2 h-2 mx-2 mt-1 rounded-full animate-ping bg-green-500"></div>
            </h1>
          </div>
          <p className="text-xs text-gray-300  mt-1">Exam Notes Generator</p>
        </div>
        <div className="flex items-center gap-2">
          <button
            onClick={() => navigate("/pricing")}
            className="flex cursor-pointer hover:bg-white/20 items-center gap-2 px-2 py-2 rounded-full bg-white/10 border border-white/20 text-white text-sm"
          >
            <span className="text-xl">üíé</span>
            <span className="text-[16px]">{credits}</span>
            <motion.span
              className="ml-2 h-5 w-5 flex items-center justify-center rounded-full bg-white text-xs text-black font-bold"
              whileHover={{ scale: 1.07 }}
              whileTap={{ scale: 1 }}
            >
              ‚ûï
            </motion.span>
          </button>
          {!isSideBarOpen && <button onClick={() => setIsSidebarOpen(true)} className="lg:hidden text-white text-2xl cursor-pointer">
            <GiHamburgerMenu className=" hover:text-white/90 text-3xl"/>
          </button>}
        </div>
      </motion.header>
      <div className="grid grid-col-1 lg:grid-cols-4 gap-6 ">
        <AnimatePresence>
          { (isSideBarOpen) && 
            <motion.div
            initial={{x: -320}}
            animate={{x: 0}}
            exit={{x: -320}}
            transition={{type: "spring" , stiffness: 260, damping:30}}
            className="fixed no-scrollbar lg:static top-0 rounded-tr-xl lg:rounded-3xl left-0 z-50 lg:z-auto w-72 lg:w-auto h-full lg:h-[75vh] lg:col-span-1 bg-black/80 lg:bg-black/80 backdrop-blur-xl border border-white/10 shadow-[0_20px_45px_rgba(0,0,0,0.6)] p-5 overflow-y-auto"
            >
              <button onClick={() => setIsSidebarOpen(false)} className="lg:hidden flex font-bold text-xl items-center justify-center hover:text-white/90 cursor-pointer gap-1 text-white my-3">
                  <IoArrowBackCircleSharp className="h-6 w-6"/> Back
              </button>
              <div className="mb-4 space-y-1">
                <button onClick={() => navigate("/notes")} className="w-full px-3 py-2 rounded-lg text-sm text-gray-200 bg-white/10 hover:bg-white/20">
                  ‚ûï New Notes
                </button>
                <hr className="border-white/10 mb-4"/>

                <h2 className="mb-4 text-lg font-bold bg-gradient-to-r from-white via-gray-300 to-white bg-clip-text text-transparent">
                  üìö Your Notes
                </h2>
                {topics.length === 0 && (
                  <p className="text-sm text-gray-400">
                    No notes created yet
                  </p>
                )}
                <ul className="space-y-3">
                  {topics.map((t,i) => (
                    <li onClick={() => {openNotes(t._id);}} key={i} className="cursor-pointer rounded-xl p-3 bg-white/5 border border-white/10 hover:bg-white/10">
                      <p className="text-sm font-semibold text-white">
                          {t.topic}
                      </p>
                      <div className="flex flex-wrap gap-2 mt-2 text-xs">
                          {t.classLevel && <span className="px-2 py-1 rounded-full bg-green-500/20 text-green-300" >ClassLevel : {t.classLevel}</span>}
                          {t.examType && <span className="px-2 py-1 rounded-full bg-blue-500/20 text-blue-300" > {t.examType}</span>}
                      </div>
                      <div className="flex gap-3 mt-2 text-xs text-gray-300">
                        {t.revisionMode && <span>Revision</span>}
                        {t.includeDiagram && <span>Diagram</span>}
                        {t.includeChart && <span>Chart</span>}
                      </div>

                    </li>
                  ))}
                </ul>
              </div>
            </motion.div>
          }
        </AnimatePresence>

        <motion.div 
        initial={{ opacity: 0, y: -15 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.6 }}
        className="lg:col-span-3 rounded-2xl bg-white shadow-[0_15px_40px_rgba(0,0,0,0.15)] p-6 min-h-[75vh]">
          {loading && <p className="text-center text-gray-500">Loading notes...</p>}
          {!loading && !selectedNote && (
            <div className="h-full flex items-center justify-center text-gray-400">
              ‚¨ÖÔ∏è Select a topic from the left or the sidebar
            </div>
          )}
          {!loading && selectedNote && (
            <FinalResult result={selectedNote}/>
          )}
        </motion.div>
      </div>
    </div>
  );
};

export default History;
